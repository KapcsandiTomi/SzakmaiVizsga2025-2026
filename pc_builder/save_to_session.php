<?php
// ============================================================================
// KONFIGURÁCIÓ FÁJL ÉS MUNKAMENET BETÖLTÉSE
// ============================================================================
require '../config.php';
session_start();

// ============================================================================
// JSON ADAT FELDOLGOZÁSA A KLIENSTŐL
// ============================================================================
$data = json_decode(file_get_contents('php://input'), true);

// ============================================================================
// KONFIGURÁCIÓ ADAT ELLENŐRZÉSE ÉS MENTÉSE A MUNKAMENETBE
// ============================================================================
if (isset($data['configuration'])) {
    // ========================================================================
    // KONFIGURÁCIÓ ADAT MENTÉSE A SESSION VÁLTOZÓBA
    // ========================================================================
    $_SESSION['pc_configuration'] = $data['configuration'];
    echo json_encode(['success' => true]);
} else {
    // ========================================================================
    // HIBAÜZENET, HA NINCS KONFIGURÁCIÓ ADAT
    // ========================================================================
    echo json_encode(['success' => false, 'error' => 'No configuration data']);
}
?>

