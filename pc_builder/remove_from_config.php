<?php
// ============================================================================
// KONFIGURÁCIÓ FÁJL ÉS MUNKAMENET BETÖLTÉSE
// ============================================================================
require '../config.php';
session_start();

// ============================================================================
// JSON ADAT FELDOLGOZÁSA A KLIENSTŐL (PHP://INPUT)
// ============================================================================
$input = file_get_contents('php://input');
$data = json_decode($input, true);

// ============================================================================
// ITEM_ID ELLENŐRZÉSE - KÖTELEZŐ MEZŐ
// ============================================================================
if (!isset($data['item_id'])) {
    http_response_code(400);
    echo json_encode(['error' => 'Missing item_id']);
    exit;
}

// ============================================================================
// ITEM_ID SZÁMMÁ KONVERTÁLÁSA (INTEGER)
// ============================================================================
$item_id = (int)$data['item_id'];

// ============================================================================
// ADATBÁZIS TÖRLÉSI LEKÉRDEZÉS ELŐKÉSZÍTÉSE
// ============================================================================
$stmt = $conn->prepare("DELETE FROM pc_configuration_items WHERE id = ?");
$stmt->bind_param("i", $item_id);

// ============================================================================
// TÖRLÉSI MŰVELET VÉGREHAJTÁSA ÉS VÁLASZ KÜLDÉSE
// ============================================================================
if ($stmt->execute()) {
    echo json_encode(['success' => true, 'deleted_id' => $item_id]);
} else {
    http_response_code(500);
    echo json_encode(['error' => 'Delete failed']);
}

// ============================================================================
// ADATBÁZIS KAPCSOLAT LEZÁRÁSA
// ============================================================================
$stmt->close();
?>